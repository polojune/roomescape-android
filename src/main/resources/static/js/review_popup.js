$(".close_btn_box").click(function() {
	$("#review_form_pop_up_back_opacity").hide();
	$("#review_form_pop_up_wrap").hide();
});

$("#rating_star").click(function() {
	$("#review_form_pop_up_back_opacity").show();
	$("#review_form_pop_up_wrap").show();
});

$("#review_content").click(function() {
	$("#review_form_pop_up_back_opacity").show();
	$("#review_form_pop_up_wrap").show();
});

$("#reviewFrm_submit").click(function() {
	$("#review_form_pop_up_back_opacity").show();
	$("#review_form_pop_up_wrap").show();
});